    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        String x = JOptionPane.showInputDialog(null, "Bitte eingeben:");
        DefaultMutableTreeNode tempNode = new DefaultMutableTreeNode(x);
        DefaultTreeModel dtm = (DefaultTreeModel) jTree1.getModel();
        TreePath tp = jTree1.getSelectionPath();
        if(tp == null || tp.getParentPath() == null){
            dtm.insertNodeInto(tempNode, (MutableTreeNode) dtm.getRoot(), dtm.getChildCount(dtm.getRoot()));
        }else{
            dtm.insertNodeInto(tempNode, (MutableTreeNode) tp.getLastPathComponent(), dtm.getChildCount(tp.getLastPathComponent()));
        }
    }//GEN-LAST:event_jMenu1MouseClicked

    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        TreePath tp = jTree1.getSelectionPath();
        DefaultTreeModel dtm = (DefaultTreeModel) jTree1.getModel();
        if(tp != null && tp.getParentPath() != null){
            dtm.removeNodeFromParent((MutableTreeNode) tp.getLastPathComponent());
        }
    }//GEN-LAST:event_jMenu2MouseClicked

    private void jMenu3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu3MouseClicked
        TreePath tp = jTree1.getSelectionPath();
        if(tp != null && tp.getParentPath() != null){
            String x = JOptionPane.showInputDialog(null, "Bitte eingeben:");
            ((DefaultMutableTreeNode)tp.getLastPathComponent()).setUserObject(x);
            DefaultTreeModel dtm = (DefaultTreeModel) jTree1.getModel();
            dtm.nodeChanged((TreeNode) tp.getLastPathComponent());
        }
    }//GEN-LAST:event_jMenu3MouseClicked


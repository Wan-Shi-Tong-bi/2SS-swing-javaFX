    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        Kunde k;
        if(help == -1){
        k = new Kunde(jTextFieldVorn.getText(), jTextFieldNachn.getText(), Integer.parseInt(jTextFieldPlz.getText()), jTextFieldStr.getText(), jTextFieldOrt.getText(), Integer.parseInt(jTextFieldNr.getText()));
        }
        else{
        k = new Kunde(jTextFieldVorn.getText(), jTextFieldNachn.getText(), Integer.parseInt(jTextFieldPlz.getText()), jTextFieldStr.getText(), jTextFieldOrt.getText(), Integer.parseInt(jTextFieldNr.getText()), help);   
        }
        DefaultTableModel dtm = (DefaultTableModel) jTableKunden.getModel();
        dtm.addRow(new String[]{""+k.getKundennummer(),k.getNachname()+", "+k.getVorname(),k.getPlz()+", "+k.getWohnort()+", "+k.getStrasse()+", "+k.getHausnr()});
        jFrame1.setVisible(false);
        kunden.put(k.getKundennummer(), k);
    }//GEN-LAST:event_jButtonOKActionPerformed

    private void jButtonAendernActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAendernActionPerformed
        if(jTableKunden.getSelectedRow()!=-1){
            Kunde k = kunden.get(Integer.parseInt((String)jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0)));
            help = k.getKundennummer();
            jTextFieldVorn.setText(k.getVorname());
            jTextFieldNachn.setText(k.getNachname());
            jTextFieldNr.setText(""+k.getHausnr());
            jTextFieldOrt.setText(k.getWohnort());
            jTextFieldStr.setText(k.getStrasse());
            jTextFieldPlz.setText(""+k.getPlz());
            jFrame1.setVisible(true);
            jButtonLoeschenActionPerformed(evt);
        }
    }//GEN-LAST:event_jButtonAendernActionPerformed

    private void jButtonLoeschenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoeschenActionPerformed
        if(jTableKunden.getSelectedRow()!=-1){
            int x = Integer.parseInt((String)jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0));
            bestellungen.remove(x);
            DefaultTableModel dtm = (DefaultTableModel) jTableKunden.getModel();
            dtm.removeRow(jTableKunden.getSelectedRow());
        }
        jTableKundenMouseClicked(null);
    }//GEN-LAST:event_jButtonLoeschenActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(jTableKunden.getSelectedRow()!=-1){
        Bestellung b = new Bestellung(Integer.parseInt(jTextField1.getText()), jTextField2.getText(), Integer.parseInt(jTextField3.getText()), Integer.parseInt(jTextField4.getText()));
       int x = Integer.parseInt((String)jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0));
       if(bestellungen.containsKey(x)){
           bestellungen.get(x).add(b);
       }
        jTableKundenMouseClicked(null);
       }else{
           JOptionPane.showMessageDialog(null, "Keine Auswahl gefunden!");           
       }
       jDialog1.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTableKundenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableKundenMouseClicked
        if(jTableKunden.getSelectedRow()!=-1){
            DefaultTableModel dtm = new DefaultTableModel(new String[]{(String)jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0)}, 0);
            jTableBestellungen.setModel(dtm);
            
            int x = Integer.parseInt((String) jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0));
            if(bestellungen.containsKey(x)){
                ArrayList<Bestellung> al = bestellungen.get((Integer.parseInt((String)jTableKunden.getValueAt(jTableKunden.getSelectedRow(), 0))));
            if(al!=null){
                for(Bestellung b: al){
                    dtm.addRow(new String[]{b.toString()});
                }
                }
            }else{
                ArrayList<Bestellung> al = new ArrayList<>();
                bestellungen.put(x, al);
            }
            
        }else{
            DefaultTableModel dtm = new DefaultTableModel();
            jTableBestellungen.setModel(dtm);
        }
    }//GEN-LAST:event_jTableKundenMouseClicked


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jSpinner1.isEnabled()==false){
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setValueAt("" + jTextField1.getText() + " " + jTextField2.getText(), jTable1.getSelectedRow(), jTable1.getSelectedColumn());
            jSpinner1.setEnabled(true);
            jSpinner2.setEnabled(true);
            jSpinner3.setEnabled(true);
            jSpinner4.setEnabled(true);
            jDialog1.setVisible(false);
        }
        else if(((LocalTime)jSpinner2.getValue()).isAfter((LocalTime)jSpinner4.getValue())){
            jLabel6.setText("Endtermin ist vor Starttermin");
        }else{
            boolean leer = true;
            for(LocalTime temp = (LocalTime)jSpinner2.getValue(); temp.isBefore(((LocalTime)jSpinner4.getValue()).plusSeconds(1)) ;temp = temp.plusMinutes(30)){
                if(jTable1.getValueAt(ltarr.indexOf(temp),Days.valueOf(jSpinner1.getValue().toString()).ordinal()+1) != null){
                    jLabel6.setText("Kollision");
                    leer = false;
                    break;
                }
            }
            if(leer){
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                for(LocalTime temp = (LocalTime)jSpinner2.getValue();  temp.isBefore(((LocalTime)jSpinner4.getValue()).plusSeconds(1)) ;temp = temp.plusMinutes(30)){
                    dtm.setValueAt("" + jTextField1.getText() + " " + jTextField2.getText(),ltarr.indexOf(temp),Days.valueOf(jSpinner1.getValue().toString()).ordinal()+1);
                }
                jDialog1.setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

